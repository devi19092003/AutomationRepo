package Tokens.operators;

public class RelationalOperators {

    public static void main(String[] args) {

        int a = 2;
        int b = 3;

        //comparison operators :-
        System.out.println((a<b));
        System.out.println((a>b));
        System.out.println((a<=b));
        System.out.println((a>=b));

        //equality :-

        System.out.println((a==b));
        System.out.println((a!=b));

    }

}
