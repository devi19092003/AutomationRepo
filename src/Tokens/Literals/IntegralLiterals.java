package Tokens.Literals;

public class IntegralLiterals {

    public static void main(String[] args) {

        //decimal literals :-
        int a = 10;
        int b = 90;
        int x = 101;
        System.out.println(7);

        //octal literals :-
        int c = 05;
        int d = 07;
        System.out.println(04);
        System.out.println(01234);
       // int e = 08; => wrong

       //Hexadecimal literals :-
       int num = 0X12;
        int num2 = 0Xc;
        int num3 = 0XC;

        System.out.println(num);

       int num1 = 0x13;
       int num4 = 0Xd;
       int num5 = 0XD;

        System.out.println(num1);

        System.out.println(0X123Face);

        //binary literals :-
        int e = 0b11;
        int f = 0B10;
        System.out.println(0b1111);
        System.out.println(0b111);
        System.out.println(0B00);
        System.out.println(0b1001);

    }

}
