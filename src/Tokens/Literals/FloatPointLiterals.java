package Tokens.Literals;

public class FloatPointLiterals {

    public static void main(String[] args) {

        //floating point literals :-
        float f = 10.25f;
        float g = 11.500028067878999989889989898808080808876578787F;

        System.out.println("float values : "+f +" "+ g);

        //float a = 90.8; => wrong

        double a = 90.8000024568585769768978707098790890890890890890897;
        double b = 56.89d;
        double c = 56.90D;

        System.out.println("Double values : "+ a + " " + b + " " + c);

    }

}
