package Tokens.Literals;

public class BackslashLiterals {

    public static void main(String[] args) {

        //new line backslash literal :-

        System.out.println("hello!!!\ngood evening to all\nhope you do practice");

        //horizontal tab :-
        System.out.println("10\t20\t30\t40\t50\t60\t70\t80\t90\t100");
        System.out.println("11\t22\t33\t44\t55\t66\t77\t88\t99\t110");

        System.out.println("srno\t\tname\t\trollno\t\taddress");
        System.out.println("1\t\t\trahul\t\t\t004\t\t\tpune");

        //single quotes:-

        System.out.println("winters are 'very cold!!!'");
        System.out.println("summers are \'very hot!!!\'");

        //double quotes:-
        //System.out.println(""elon musk" takes over "twitter"!!!");
        System.out.println("elon musk takes over \"twitter\"!!!");

        //backspace literal :-
        /* it is used to remove spaces as well as
        * any character present */
        System.out.println("twitter layoffs    \b\b\b\b have       \b\b\b\b\bbegun");
        System.out.println("cookiessss\b\b\b");
    }


}
